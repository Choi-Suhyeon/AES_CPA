uint32_t ror32(uint32_t v, uint32_t n) {
    return (v >> n) | (v << (32 - n));
}

void rol32_u8s(uint8_t * v, uint32_t n) {
    uint32_t r = ror32(*(uint32_t *)v, n);
    memmove(v, &r, sizeof r);
}

void sub_word(uint8_t * x) {
    x[0] = kSBox[x[0]];
    x[1] = kSBox[x[1]];
    x[2] = kSBox[x[2]];
    x[3] = kSBox[x[3]];
}

void xor_word(const uint8_t * x, const uint8_t * y, uint8_t * out) {
    *(uint32_t *)out = *(uint32_t *)x ^ *(uint32_t *)y;
}

uint8_t calc_hw(uint8_t byte) {
    uint8_t hw = 0;

    for (size_t i = 0; i < 8; i++) {
        hw += (byte >> i) & 1;
    }

    return hw;
}

void recover_aes128_master_key(const uint8_t * last_rk, uint8_t * out) {
    uint8_t
        interim1[0x10] = { 0, },
        interim2[0x10] = { 0, },
        t[4]           = { 0, };

    memmove(interim1, last_rk, sizeof interim1);

    for (signed r = 9; r >= 0; r--) {
        uint8_t
            * from = r & 1 ? interim1 : interim2,
            * to   = r & 1 ? interim2 : interim1;

        xor_word(from + 0x8, from + 0xC, to + 0xC);
        xor_word(from + 0x4, from + 0x8, to + 0x8);
        xor_word(from + 0x0, from + 0x4, to + 0x4);

        memmove(t, to + 0xC, sizeof t);
        rol32_u8s(t, 8);
        sub_word(t);
        t[0] ^= kRCon[r];

        xor_word(t, from + 0x0, to + 0x0);
    }

    memmove(out, interim1, sizeof interim1);
}

bool perform_cpa(uint8_t * out) {
    double  * trace      = NULL;
    uint8_t * ciphertext = NULL;

    FILE
        * rfp_cipher = NULL,
        * rfp_trace  = NULL,
        * wfp_corr   = NULL;

    if (!(rfp_cipher = fopen(CIPHERTEXT_FN, "rb"))) {
        puts("failed to open ciphertext");
        return false;
    }

    if (!(rfp_trace = fopen(TRACE_FN, "rb"))) {
        puts("failed to open trace");
        fclose(rfp_cipher);
        return false;
    }

    if (!(trace = calloc(TRACE_CNT * TRACE_LEN, sizeof *trace))) {
        puts("failed to alloc memory for trace");
        fclose(rfp_cipher);
        fclose(rfp_trace);
        return false;
    }

    if (!(ciphertext = calloc(CIPHERTEXT_CNT, CIPHERTEXT_LEN))) {
        puts("failed to alloc memory");
        fclose(rfp_cipher);
        fclose(rfp_trace);
        free(trace);
        return false;
    }

    fread(trace, sizeof *trace, TRACE_CNT * TRACE_LEN, rfp_trace);
    fread(ciphertext, CIPHERTEXT_LEN, CIPHERTEXT_CNT, rfp_cipher);

    fclose(rfp_trace);
    fclose(rfp_cipher);

    double
        * s_x    = calloc(TRACE_LEN, sizeof *s_x),
        * s_xx   = calloc(TRACE_LEN, sizeof *s_xx),
        * s_xy   = calloc(TRACE_LEN, sizeof *s_xy),
        * corr_t = calloc(TRACE_LEN, sizeof *corr_t);

    for (size_t t = 0; t < TRACE_CNT; t++) {
        for (size_t i = START_POINT; i < END_POINT; i++) {
            s_x[i] += trace[t * TRACE_LEN + i];
            s_xx[i] += trace[t * TRACE_LEN + i] * trace[t * TRACE_LEN + i];
        }
    }

    for (size_t nth = 0; nth < CIPHERTEXT_LEN; nth++) {
        clock_t start_time = clock();
        double  max_val    = 0;
        size_t  max_key    = 0;

        for (size_t key = 0; key <= UCHAR_MAX; key++) {
            double
                s_y  = 0,
                s_yy = 0;

            memset(s_xy, 0, TRACE_LEN * sizeof *s_xy);

            for (size_t t = 0; t < TRACE_CNT; t++) {
                uint8_t
                    iv    = kInvSBox[key ^ ciphertext[t * CIPHERTEXT_LEN + nth]],
                    iv_hw = calc_hw(iv);

                s_y  += iv_hw;
                s_yy += iv_hw * iv_hw;

                for (size_t i = START_POINT; i < END_POINT; i++) {
                    s_xy[i] += (double)iv_hw * trace[t * TRACE_LEN + i];
                }
            }

            for (size_t i = START_POINT; i < END_POINT; i++) {
                corr_t[i] = fabs(
                    ((double)TRACE_CNT * s_xy[i] - s_x[i] * s_y) /
                    sqrt(((double)TRACE_CNT * s_xx[i] - s_x[i] * s_x[i]) * ((double)TRACE_CNT * s_yy - s_y * s_y))
                );

                if (corr_t[i] > max_val) {
                    max_key = key;
                    max_val = corr_t[i];
                }
            }

            char buffer[BUF_SZ] = { 0, };

            sprintf(buffer, "corr_traces\\%02zuth_block_%02zx.corrtrace", nth, key);

            if (!(wfp_corr = fopen(buffer, "wb"))) {
                printf("failed to write to %256s\n", buffer);
                continue;
            }

            fwrite(corr_t, sizeof *corr_t, TRACE_LEN, wfp_corr);
            fclose(wfp_corr);
        }

        out[nth] = (uint8_t)max_key;

        clock_t end_time     = clock();
        double  elapsed_time = ((double)(end_time - start_time)) / CLOCKS_PER_SEC;

        printf("- %02zuth_block_%02zx (corr : %lf) (elapsed : %.4lf)\n", nth, max_key, max_val, elapsed_time);
    }

    free(s_x);
    free(s_xx);
    free(s_xy);
    free(trace);
    free(corr_t);
    free(ciphertext);

    return true;
}

int main(void) {
    uint8_t
        last_rk[0x10] = { 0, },
        mk[0x10]      = { 0, };

    if (!perform_cpa(last_rk)) {
        puts("failed to perform cpa");
        return 1;
    }

    recover_aes128_master_key(last_rk, mk);

    printf("\nMaster Key :");

    for (int i = 0; i < sizeof mk; i++) {
        printf(" %02X", mk[i]);
    }

    puts("");

    return 0;
}